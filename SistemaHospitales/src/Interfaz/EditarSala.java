    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cod = ElegirCiudad.codCiudad;
        String rolHospital = ElegirHospital.rolHospital;

        int num;
        boolean numb;
        try {
            num = Integer.parseInt(cantidad.getText());
            numb = true;
        } catch (Exception e) {
            numb = false;
        }
        if (idNuevo.getText().length() != 0 && ubicacion.getText().length() != 0 && cantidad.getText().length() != 0) {
            if (numb) {
                if (MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getSalas().modificarSala(new Sala(idNuevo.getText(), ubicacion.getText(), Integer.parseInt(cantidad.getText())))) {
                    JOptionPane.showMessageDialog(null, "Sala actualizada");
                } else {
                    JOptionPane.showMessageDialog(null, "Sala no existe, intente nuevamente");
                }
            } else {
                JOptionPane.showMessageDialog(null, "cantidad de camillas debe ser numerico");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar todos los campos");
        }
        
        
        Connection cn = Conectar.getConnection();
        PreparedStatement ps;
        
        String sql = "UPDATE sala SET ubicacion = ?, cant = ?,codCiudad = ?,codHospital =?  WHERE cod = ?";
        
        
        
        try {
            ps = cn.prepareStatement(sql);
            ps.setString(1, ubicacion.getText());
            ps.setInt(2, Integer.parseInt(cantidad.getText()));
            ps.setString(3, ElegirCiudad.codCiudad);
            ps.setString(4, ElegirHospital.rolHospital);
            ps.setString(5, idNuevo.getText() );
            ps.executeUpdate();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL GUARDAR LA SALA EN BD:" + e);
        }
        
        ubicacion.setText(null);
        cantidad.setText(null);
        idNuevo.setText(null);

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

