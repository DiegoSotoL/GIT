    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String cod = ElegirCiudad.codCiudad;
        String rolHospital = ElegirHospital.rolHospital;

        if (MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getSalas().existeSala(id.getText())) {
            ubicacion.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getSalas().getSala(id.getText()).getUbicacion());
            cantidad.setText(Integer.toString(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getSalas().getSala(id.getText()).getCantCamillas()));
            idNuevo.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getSalas().getSala(id.getText()).getCod());
            idNuevo.setEditable(false);

        } else {
            JOptionPane.showMessageDialog(null, "Sala no existe, intente nuevamente");
            ubicacion.setText(null);
            cantidad.setText(null);
            idNuevo.setText(null);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

