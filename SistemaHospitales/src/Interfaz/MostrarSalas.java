    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        DefaultTableModel modelo = (DefaultTableModel) salas.getModel();
        String cod = ElegirCiudad.codCiudad;
        String codHospital = ElegirHospital.rolHospital;
        ListaSalas LS = MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(codHospital).getSalas();

        modelo.addColumn("CODIGO");
        modelo.addColumn("UBICACION");
        modelo.addColumn("CANTIDAD DE CAMILLAS");

        for (int i = 0; i < LS.cantidadSalas(); i++) {
            Object row[] = {LS.getSala(i).getCod(), LS.getSala(i).getUbicacion(), LS.getSala(i).getCantCamillas()};
            System.out.println(LS.getSala(i).getCod());
            ((DefaultTableModel) salas.getModel()).addRow(row);
        }

// TODO add your handling code here:
    }//GEN-LAST:event_formWindowOpened

