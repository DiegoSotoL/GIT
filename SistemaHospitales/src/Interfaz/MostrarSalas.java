    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String cod = ElegirCiudad.codCiudad;
        String codHospital = ElegirHospital.rolHospital;

        FileWriter w = null;
        try {
            ListaSalas LS = MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(codHospital).getSalas();
            File f;
            f = new File("Salas del hospital "+MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(codHospital).getNombre()+".txt");
            w = new FileWriter(f);
            BufferedWriter bw = new BufferedWriter(w);
            for (int i = 0; i < LS.cantidadSalas(); i++) {

                try {

                    bw.write("CODIGO: " + LS.getSala(i).getCod());
                    bw.newLine();
                    bw.write("UBICACION: " + LS.getSala(i).getUbicacion());
                    bw.newLine();
                    bw.write("CANTIDAD DE CAMILLAS: " + LS.getSala(i).getCantCamillas());
                    bw.newLine();
                    bw.newLine();
                } catch (IOException e) {
                };

            }
            bw.close();

            // TODO add your handling code here:
        } catch (IOException ex) {
            Logger.getLogger(MostrarCiudades.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                w.close();
            } catch (IOException ex) {
                Logger.getLogger(MostrarCiudades.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

