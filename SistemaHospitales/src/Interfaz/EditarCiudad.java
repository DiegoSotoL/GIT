    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (MenuPrincipal.SH.getCiudades().existeCiudad(codCambio.getText())) {
            nombre.setText(MenuPrincipal.SH.getCiudades().getCiudad(codCambio.getText()).getNombre());
            region.setText(MenuPrincipal.SH.getCiudades().getCiudad(codCambio.getText()).getRegion());
            codigo.setText(MenuPrincipal.SH.getCiudades().getCiudad(codCambio.getText()).getCodigoPostal());
            codigo.setEditable(false);

        } else {
            JOptionPane.showMessageDialog(null, "Ciudad no existe, intente nuevamente");
            nombre.setText(null);
            region.setText(null);
            codigo.setText(null);
        }


    }//GEN-LAST:event_jButton3ActionPerformed

