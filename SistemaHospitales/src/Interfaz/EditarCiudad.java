    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (nombre.getText().length() != 0 && region.getText().length() != 0 && codigo.getText().length() != 0) { //si se ingresan todos los parametros pasa
            if (MenuPrincipal.SH.getCiudades().modificarCiudad(new Ciudad(nombre.getText(), region.getText(), codigo.getText()))) {//si el metodo modificar de listaciudades retorna true pasa(edicion exitosa)
                JOptionPane.showMessageDialog(null, "Ciudad actualizada");
            } else {
                JOptionPane.showMessageDialog(null, "Ciudad no existe, intente nuevamente");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar todos los campos");
        }

        //update realizado en la base de datos de la ciudad en cuestion
        
        Connection cn = Conectar.getConnection();
        PreparedStatement ps;
        String sql = "UPDATE ciudad SET nombre = ?, region = ? WHERE codigoPostal = ?";
        
        try {
            ps = cn.prepareStatement(sql);
            ps.setString(1, nombre.getText() );
            ps.setString(2, region.getText());
            ps.setString(3, codigo.getText());
            ps.executeUpdate();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL GUARDAR CIUDAD EN BD" + e);
        }

        
        
        nombre.setText(null);
        region.setText(null);
        codigo.setText(null);

        
    }//GEN-LAST:event_jButton1ActionPerformed

