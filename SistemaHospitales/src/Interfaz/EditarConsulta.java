    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cod = ElegirCiudad.codCiudad;
        String rolHospital = ElegirHospital.rolHospital;
        String rutDoctor = ElegirDoctor.rutDoctor;

        if (idNuevo.getText().length() != 0 && codSala.getText().length() != 0 && fecha.getText().length() != 0 && rutPaciente.getText().length() != 0 && rutDoc.getText().length() != 0 && motivo.getText().length() != 0) {
            if (MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().modificarConsulta(new Consulta(idNuevo.getText(), codSala.getText(), rutPaciente.getText(), rutDoc.getText(), fecha.getText(), motivo.getText()))) {
                JOptionPane.showMessageDialog(null, "Consulta actualizada");
            } else {
                JOptionPane.showMessageDialog(null, "Consulta no existe, intente nuevamente");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar todos los campos");
        }

        
        Connection cn = Conectar.getConnection();
        PreparedStatement ps;

        String sql = "UPDATE consultas SET codSala = ?, rutPaciente = ?, fecha = ?, motivo = ?, codCiudad = ?, codHospital = ?, codDoctor = ? WHERE id = ?";

        try {
            ps = cn.prepareStatement(sql);
            ps.setString(1, codSala.getText());
            ps.setString(2, rutPaciente.getText());
            ps.setString(3, fecha.getText());
            ps.setString(4, motivo.getText());
            ps.setString(5, ElegirCiudad.codCiudad);
            ps.setString(6, ElegirHospital.rolHospital);
            ps.setString(7, ElegirDoctor.rutDoctor);
            ps.setString(8, idNuevo.getText());
            ps.executeUpdate();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL GUARDAR CIUDAD EN BD" + e);
        }

        codSala.setText(null);
        rutPaciente.setText(null);
        rutDoc.setText(null);
        fecha.setText(null);
        motivo.setText(null);
        idNuevo.setText(null);

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

