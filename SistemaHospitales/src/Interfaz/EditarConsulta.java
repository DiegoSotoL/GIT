    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String cod = ElegirCiudad.codCiudad;
        String rolHospital = ElegirHospital.rolHospital;
        String rutDoctor = ElegirDoctor.rutDoctor;

        if (MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().existeConsulta(id.getText())) {
            codSala.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().getConsulta(id.getText()).getCodSala());
            rutPaciente.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().getConsulta(id.getText()).getRutPaciente());
            rutDoc.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().getConsulta(id.getText()).getRutDoctor());
            fecha.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().getConsulta(id.getText()).getFechaHora());
            motivo.setText(MenuPrincipal.SH.getCiudades().getCiudad(cod).getHospitales().getHospital(rolHospital).getDoctores().getDoctor(rutDoctor).getConsultas().getConsulta(id.getText()).getMotivoConsulta());
            idNuevo.setText(id.getText());
            idNuevo.setEditable(false);

        } else {
            JOptionPane.showMessageDialog(null, "Consulta no existe, intente nuevamente");
            codSala.setText(null);
            rutPaciente.setText(null);
            rutDoc.setText(null);
            fecha.setText(null);
            motivo.setText(null);
            idNuevo.setText(null);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

